
public class SomaDosQuadrados {
	public static void main(String args[]) {
		int a, b, c, quadradoa, quadradob, quadradoc, soma;
		a = 5;
		b = 7;
		c = 4;
		quadradoa = a*a;
		quadradob = b*b;
		quadradoc = c*c;
		soma = quadradoa + quadradob + quadradoc;
		System.out.println("A soma dos quadrados é igual à: "+soma);
	}
}