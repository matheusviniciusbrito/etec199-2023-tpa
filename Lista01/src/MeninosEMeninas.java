
public class MeninosEMeninas {
	public static void main(String args[]) {
		double meninos, meninas, total, porcmeninas, porcmeninos;
		meninos = 5;
		meninas = 10;
		total = meninos + meninas;
		porcmeninas = (meninas*100)/total;
		porcmeninos = (meninos*100)/total;
		System.out.println("Porcentagem de meninas: " + porcmeninas + "%");
		System.out.println("Porcentagem de meninos: " + porcmeninos + "%");
	}
}
